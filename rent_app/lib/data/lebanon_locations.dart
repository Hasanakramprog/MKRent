  static List<String> getAllCities() {
    List<String> allCities = [];
    governoratesAndCities.forEach((governorate, cities) {
      allCities.addAll(cities);
    });
    // Remove duplicates and sort
    return allCities.toSet().toList()..sort();
  }

  static List<String> getCitiesByGovernorate(String governorate) {
    return governoratesAndCities[governorate] ?? [];
  }

  static List<String> getGovernorates() {
    return governoratesAndCities.keys.toList();
  }

  static String? getGovernorateByCity(String city) {
    for (String governorate in governoratesAndCities.keys) {
      if (governoratesAndCities[governorate]!.contains(city)) {
        return governorate;
      }
    }
    return null;
  }

  static List<String> searchCities(String query) {
    if (query.isEmpty) return getAllCities();
    
    final lowercaseQuery = query.toLowerCase();
    return getAllCities()
        .where((city) => city.toLowerCase().contains(lowercaseQuery))
        .toList();
  }
